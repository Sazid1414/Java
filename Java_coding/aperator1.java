public class aperator1 {
    public static void main(String[] args)
    {
        //operator 
        //Arithmetic_Operator
        int a=1;
        int b=2;
        int sum=a+b;
        int sub=a-b;
        int mul=a*b;
        int div=a/b;
        int mod=a%b;
        System.out.println(sum);
        System.out.println(sub);
        System.out.println(mul);
        System.out.println(div);
        System.out.println(mod);
        //unary operator
        int num=1;
        
        System.out.println(num++);


        
        System.out.println(num);
        System.out.println(++num);

    }
}
